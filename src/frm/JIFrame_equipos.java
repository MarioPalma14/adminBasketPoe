    private void jbAggActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAggActionPerformed
        if(this.jtCod.getText().trim().equals("") || this.jtNombre.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Debe agregar informaci贸n en los campos.");
        }
        else{
            try {
            String sqlInsert = query.ingresar_equipo(Integer.parseInt(this.jtCod.getText()), this.jtNombre.getText().trim().toUpperCase());
            cn.query_ingreso_actu_elim(sqlInsert);
            this.cargar();
            this.limpiar();
            } catch (SQLException ex) {
                Logger.getLogger(JIFrame_equipos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }       
    }//GEN-LAST:event_jbAggActionPerformed

    private void jbModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModActionPerformed
        if (this.jTable1.getSelectedRow() != -1)
        {
            if (this.jtCod.getText().trim().equals("") || this.jtNombre.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe agregar informaci贸n en los campos.");
            }
            else{
                try {
                    String sqlUpdate = query.actu_equipo(Integer.parseInt(this.jtCod.getText()), this.jtNombre.getText().trim().toUpperCase());
                    cn.query_ingreso_actu_elim(sqlUpdate);
                    this.cargar();
                    this.limpiar();
                } catch (SQLException ex) {
                    Logger.getLogger(JIFrame_equipos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }   
        }
        else{
            JOptionPane.showMessageDialog(null, "Debe selecionar un registro.");
        }
        
    }//GEN-LAST:event_jbModActionPerformed

    private void JbElimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbElimActionPerformed
        if (this.jTable1.getSelectedRow() != -1){
            try {
                String sqlDelete = query.eliminar_equipo(Integer.parseInt(this.jtCod.getText()));
                cn.query_ingreso_actu_elim(sqlDelete);
                this.cargar();
                this.limpiar();
            } catch (SQLException ex) {
                Logger.getLogger(JIFrame_equipos.class.getName()).log(Level.SEVERE, null, ex);
            }
         }
         else{
            JOptionPane.showMessageDialog(null, "Debe selecionar un registro.");
        }
       
    }//GEN-LAST:event_JbElimActionPerformed

