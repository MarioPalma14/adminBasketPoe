    public void cargar(){
        ResultSet rs = null;
        try {
            DefaultTableModel model = new DefaultTableModel();
            String[] nombreColum = {"C贸digo", "Nombre"};
            model.setColumnIdentifiers(nombreColum);
//            mpwt_conexion conex = new mpwt_conexion("jdbc:mysql://localhost:3306/clase08", "root", "");

            rs = cn.query(query.equipos());
            String nombre;
            int codigo;

            while (rs.next()) {
                codigo = rs.getInt("codigo");
                nombre = rs.getString("nombre");
                model.addRow(new Object[]{codigo, nombre});
            }
            this.jTable1.setModel(model);
            this.jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
            this.jTable1.setFillsViewportHeight(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
