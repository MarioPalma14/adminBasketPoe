    private void jButton_eleminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eleminarActionPerformed
        String id_unive;
        if (this.jTable1.getSelectedRow() > -1) {
            try {
                id_unive = String.valueOf(this.model.getValueAt(this.jTable1.getSelectedRow(), 0));
                int id = Integer.parseInt(id_unive);
                conex.query_ingreso_actu_elim(consulta.eliminar_universidad(id));
                this.cargar();
                this.limpiar();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error! La universidad seleccionada ya contiene relaciones");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Error! Debe elegir un dato de la lista");
        }
    }//GEN-LAST:event_jButton_eleminarActionPerformed


