    private void jButton_agregar_uniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_agregar_uniActionPerformed
        String universidad;  
        if (this.jTextField_universidad.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Error! Debe ingresar un nombre");
        }
        else{
            try {
                universidad = this.jTextField_universidad.getText().trim().toUpperCase();

                conex.query_ingreso_actu_elim(consulta.ingresar_univer(universidad));
                this.cargar();
                this.limpiar();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_jButton_agregar_uniActionPerformed

    private void jButton_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_modificarActionPerformed
        String universidad, id_unive;
        
        if (this.jTable1.getSelectedRow() > -1) {
            if (this.jTextField_universidad.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Error! Debe ingresar un nombre");
            }
            else{
                try {
                    id_unive = String.valueOf(this.model.getValueAt(this.jTable1.getSelectedRow(), 0));
                    int id = Integer.parseInt(id_unive);
                    universidad = this.jTextField_universidad.getText().trim().toUpperCase();
                    conex.query_ingreso_actu_elim(consulta.modificar_univer(id, universidad));

                    this.cargar();
                    this.limpiar();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.toString());
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Error! Debe elegir un dato de la lista");
        }
    }//GEN-LAST:event_jButton_modificarActionPerformed

