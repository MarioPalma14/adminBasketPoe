    public void cargar(){
        ResultSet rs = null;
        try {
            DefaultTableModel model = new DefaultTableModel();
            String[] nombreColum = {"CÃ³digo", "Nombre","Jugadores"};
            model.setColumnIdentifiers(nombreColum);

            rs = cn.query(query.equipos_oficiales());
            String nombre;
            int codigo, jugadores;

            while (rs.next()) {
                codigo = rs.getInt("codigo");
                nombre = rs.getString("nombre");
                jugadores = rs.getInt("jugadores");
                model.addRow(new Object[]{codigo, nombre, jugadores});
            }
            this.jTable1.setModel(model);
            this.jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
            this.jTable1.setFillsViewportHeight(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
